CREATE TABLE youtube_information (
    channels_rank INTEGER,
    youtuber VARCHAR(255),
    subscribers BIGINT,
    video_views BIGINT,
    category VARCHAR(255),
    title VARCHAR(255),
    uploads INT,
    country VARCHAR(255),
    abbreviation VARCHAR(10),
    channel_type VARCHAR(50),
    countrycode VARCHAR(10),
    continent VARCHAR(50),
    videos_gained BIGINT,
    videos_lost BIGINT,
    videos_deleted BIGINT,
    subscribers_gained BIGINT,
    subscribers_lost BIGINT,
    subscribers_for_last_30_days BIGINT,
    created_year INT,
    created_month VARCHAR(10),
    created_date INT,
    gross_tertiary_education_enrollment DECIMAL(5, 2),
    population BIGINT,
    unemployment_rate DECIMAL(5, 2),
    urban_population BIGINT,
    latitude DECIMAL(10, 6),
    longitude DECIMAL(10, 6)
);
